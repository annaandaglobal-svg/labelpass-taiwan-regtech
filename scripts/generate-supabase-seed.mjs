import { createHash } from "node:crypto";
import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const root = process.cwd();
const rulesPath = path.join(root, "data", "rules", "tw-cosmetics-rules.json");
const outPath = path.join(root, "supabase", "seed.sql");

function sql(value) {
  if (value === null || value === undefined) return "null";
  return `'${String(value).replaceAll("'", "''")}'`;
}

function jsonSql(value) {
  return `${sql(JSON.stringify(value))}::jsonb`;
}

function stableUuid(input) {
  const hex = createHash("sha256").update(input).digest("hex");
  return [
    hex.slice(0, 8),
    hex.slice(8, 12),
    `5${hex.slice(13, 16)}`,
    ((parseInt(hex.slice(16, 18), 16) & 0x3f) | 0x80).toString(16).padStart(2, "0") + hex.slice(18, 20),
    hex.slice(20, 32)
  ].join("-");
}

const payload = JSON.parse(await readFile(rulesPath, "utf8"));
const generatedAt = payload.generated_at.slice(0, 10);
const sources = new Map();

for (const rule of payload.rules) {
  const sourceId = stableUuid(`source:${rule.source_info_id}`);
  if (!sources.has(rule.source_info_id)) {
    sources.set(rule.source_info_id, {
      id: sourceId,
      info_id: rule.source_info_id,
      title: rule.source_title,
      source_url: rule.source_url,
      export_url: rule.export_url,
      category: rule.category
    });
  }
}

const lines = [
  "-- Generated by scripts/generate-supabase-seed.mjs",
  `-- Source rules generated at: ${payload.generated_at}`,
  "begin;",
  ""
];

for (const source of sources.values()) {
  lines.push(
    `insert into public.regulatory_sources (id, authority, jurisdiction, source_type, title, source_url, external_id, info_id, source_format, update_frequency, last_checked_at, is_active, notes) values (` +
      [
        sql(source.id),
        sql("TFDA"),
        sql("TW"),
        sql("dataset"),
        sql(source.title),
        sql(source.source_url),
        sql(`InfoId ${source.info_id}`),
        source.info_id,
        sql("json"),
        sql("manual refresh / scheduled crawler"),
        sql(payload.generated_at),
        "true",
        sql(`JSON export: ${source.export_url}`)
      ].join(", ") +
      `) on conflict (source_url) do update set title = excluded.title, info_id = excluded.info_id, last_checked_at = excluded.last_checked_at, notes = excluded.notes;`
  );
}

lines.push("");

for (const rule of payload.rules) {
  const sourceId = stableUuid(`source:${rule.source_info_id}`);
  const ruleId = stableUuid(`rule:${rule.id}`);
  const versionLabel = rule.raw_hash.slice(0, 16);
  const versionId = stableUuid(`rule-version:${rule.id}:${versionLabel}`);
  const title = rule.ingredient_name || rule.inci_names?.[0] || rule.id;
  const ingredientNames = [rule.ingredient_name, ...(rule.inci_names || [])].filter(Boolean);

  lines.push(
    `insert into public.rules (id, source_id, rule_code, title, category, jurisdiction, match_strategy, status, scope) values (` +
      [
        sql(ruleId),
        sql(sourceId),
        sql(rule.id),
        sql(title),
        sql(rule.category),
        sql("TW"),
        sql(rule.cas_numbers?.length ? "cas" : rule.color_index_numbers?.length ? "color_index" : "ingredient"),
        sql("active"),
        jsonSql({ product_scope: rule.product_scope, source_info_id: rule.source_info_id })
      ].join(", ") +
      `) on conflict (rule_code) do update set title = excluded.title, category = excluded.category, scope = excluded.scope;`
  );

  lines.push(
    `update public.rule_versions set is_current = false, effective_to = ${sql(generatedAt)} where rule_id = ${sql(ruleId)} and id <> ${sql(versionId)} and is_current = true;`
  );

  lines.push(
    `insert into public.rule_versions (id, rule_id, source_id, version_label, source_record_id, source_hash, effective_from, is_current, rule_text, ingredient_names, cas_numbers, color_index_numbers, product_scope, limit_text, restriction_text, caution_text, conditions, raw_payload, source_snapshot) values (` +
      [
        sql(versionId),
        sql(ruleId),
        sql(sourceId),
        sql(versionLabel),
        sql(rule.source_record_id),
        sql(rule.raw_hash),
        sql(generatedAt),
        "true",
        sql([rule.ingredient_name, rule.limit_text, rule.restriction_text, rule.caution_text, rule.notes].filter(Boolean).join(" | ")),
        jsonSql(ingredientNames),
        jsonSql(rule.cas_numbers || []),
        jsonSql(rule.color_index_numbers || []),
        sql(rule.product_scope),
        sql(rule.limit_text),
        sql(rule.restriction_text),
        sql(rule.caution_text),
        jsonSql({ limit_percent_values: rule.limit_percent_values, max_limit_percent: rule.max_limit_percent }),
        jsonSql(rule.raw),
        jsonSql({ source_url: rule.source_url, export_url: rule.export_url, source_info_id: rule.source_info_id })
      ].join(", ") +
      `) on conflict (rule_id, version_label) do update set is_current = true, effective_to = null, rule_text = excluded.rule_text, raw_payload = excluded.raw_payload, source_snapshot = excluded.source_snapshot;`
  );
}

lines.push("", "commit;", "");

await mkdir(path.dirname(outPath), { recursive: true });
await writeFile(outPath, lines.join("\n"), "utf8");
console.log(JSON.stringify({ output: "supabase/seed.sql", rules: payload.rules.length, sources: sources.size }, null, 2));
