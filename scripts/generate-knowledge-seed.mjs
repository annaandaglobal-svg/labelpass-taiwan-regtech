import { readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const root = process.cwd();
const registryPath = path.join(root, "data", "knowledge", "source-registry.json");
const indexPath = path.join(root, "data", "knowledge", "index.json");
const termIndexPath = path.join(root, "data", "knowledge", "term-index.json");
const updateQueuePath = path.join(root, "data", "knowledge", "regulatory-update-queue.json");
const outPath = path.join(root, "supabase", "knowledge-seed.sql");

function sql(value) {
  if (value === null || value === undefined) return "null";
  return `'${String(value).replaceAll("'", "''")}'`;
}

function jsonSql(value) {
  return `${sql(JSON.stringify(value))}::jsonb`;
}

const registry = JSON.parse(await readFile(registryPath, "utf8"));
const index = JSON.parse(await readFile(indexPath, "utf8"));
const termIndex = JSON.parse(await readFile(termIndexPath, "utf8"));
const updateQueue = JSON.parse(await readFile(updateQueuePath, "utf8"));
const sources = new Map(registry.sources.map((source) => [source.id, source]));

const lines = [
  "-- Generated by scripts/generate-knowledge-seed.mjs",
  `-- Knowledge index generated at: ${index.generated_at}`,
  "begin;",
  "delete from public.term_rule_links;",
  "delete from public.term_aliases;",
  "delete from public.knowledge_terms;",
  "delete from public.regulatory_update_candidates;",
  "delete from public.knowledge_snapshots;",
  "delete from public.knowledge_sources;",
  ""
];

for (const result of index.results) {
  const source = sources.get(result.id);
  if (!source) continue;

  const docPath = path.join(root, result.document_path);
  const doc = await readFile(docPath, "utf8");
  const extract = doc.split("## Extract")[1]?.trim().slice(0, 12000) ?? "";

  lines.push(
    `insert into public.knowledge_sources (source_key, title, source_url, authority, jurisdiction, domain, source_type, priority, tags, cache_days, is_active) values (` +
      [
        sql(source.id),
        sql(source.title),
        sql(source.url),
        sql(source.authority),
        sql(source.jurisdiction),
        sql(source.domain),
        sql(source.source_type),
        sql(source.priority),
        jsonSql(source.tags),
        source.cache_days ?? registry.default_cache_days ?? 14,
        "true"
      ].join(", ") +
      `) on conflict (source_key) do update set title = excluded.title, source_url = excluded.source_url, authority = excluded.authority, jurisdiction = excluded.jurisdiction, domain = excluded.domain, source_type = excluded.source_type, priority = excluded.priority, tags = excluded.tags, cache_days = excluded.cache_days, is_active = true;`
  );

  lines.push(
    `insert into public.knowledge_snapshots (source_key, content_hash, fetched_at, from_cache, bytes, text_chars, document_path, extract, metadata) values (` +
      [
        sql(result.id),
        sql(result.content_hash),
        sql(result.fetched_at),
        result.from_cache ? "true" : "false",
        result.bytes,
        result.text_chars,
        sql(result.document_path),
        sql(extract),
        jsonSql({
          source_registry_version: index.source_registry_version,
          authority: result.authority,
          domain: result.domain,
          tags: result.tags,
          format: result.format,
          page_count: result.page_count,
          parse_error: result.parse_error,
          fetched_url: result.fetched_url,
          extra_fetched_urls: result.extra_fetched_urls ?? [],
          cache_days: result.cache_days ?? source.cache_days ?? registry.default_cache_days ?? 14,
          cache_expires_at: result.cache_expires_at ?? null,
          cache_status: result.cache_status ?? null,
          browser_capture_path: result.browser_capture_path ?? null,
          screenshot_path: result.screenshot_path ?? null
        })
      ].join(", ") +
      `) on conflict (source_key, content_hash) do update set fetched_at = excluded.fetched_at, from_cache = excluded.from_cache, bytes = excluded.bytes, text_chars = excluded.text_chars, document_path = excluded.document_path, extract = excluded.extract, metadata = excluded.metadata;`
  );
}

lines.push("");
lines.push("-- Regulatory source update candidates");

for (const item of updateQueue.items ?? []) {
  lines.push(
    `insert into public.regulatory_update_candidates (` +
      [
        "candidate_key",
        "source_key",
        "title",
        "source_url",
        "authority",
        "jurisdiction",
        "domain",
        "source_type",
        "source_priority",
        "change_type",
        "severity",
        "status",
        "detected_at",
        "fetched_at",
        "cache_expires_at",
        "previous_hash",
        "current_hash",
        "affected_domains",
        "affected_terms",
        "affected_products",
        "evidence",
        "next_action",
        "reviewer_notes",
        "decision",
        "decided_at",
        "metadata"
      ].join(", ") +
      `) values (` +
      [
        sql(item.candidate_key),
        sql(item.source_key),
        sql(item.title),
        sql(item.source_url),
        sql(item.authority),
        sql(item.jurisdiction),
        sql(item.domain),
        sql(item.source_type),
        sql(item.source_priority),
        sql(item.change_type),
        sql(item.severity),
        sql(item.status),
        sql(item.detected_at),
        sql(item.fetched_at),
        sql(item.cache_expires_at),
        sql(item.previous_hash),
        sql(item.current_hash),
        jsonSql(item.affected_domains ?? []),
        jsonSql(item.affected_terms ?? []),
        jsonSql(item.affected_products ?? []),
        jsonSql(item.evidence ?? {}),
        sql(item.next_action),
        sql(item.reviewer_notes),
        sql(item.decision),
        sql(item.decided_at),
        jsonSql({
          ...(item.metadata ?? {}),
          update_queue_generated_at: updateQueue.generated_at,
          crawl_generated_at: updateQueue.crawl_generated_at
        })
      ].join(", ") +
      `) on conflict (candidate_key) do update set source_key = excluded.source_key, title = excluded.title, source_url = excluded.source_url, authority = excluded.authority, jurisdiction = excluded.jurisdiction, domain = excluded.domain, source_type = excluded.source_type, source_priority = excluded.source_priority, change_type = excluded.change_type, severity = excluded.severity, status = excluded.status, detected_at = excluded.detected_at, fetched_at = excluded.fetched_at, cache_expires_at = excluded.cache_expires_at, previous_hash = excluded.previous_hash, current_hash = excluded.current_hash, affected_domains = excluded.affected_domains, affected_terms = excluded.affected_terms, affected_products = excluded.affected_products, evidence = excluded.evidence, next_action = excluded.next_action, reviewer_notes = excluded.reviewer_notes, decision = excluded.decision, decided_at = excluded.decided_at, metadata = excluded.metadata;`
  );
}

lines.push("");
lines.push("-- Canonical regulatory terms, aliases, and rule links");

for (const term of termIndex.terms ?? []) {
  lines.push(
    `insert into public.knowledge_terms (term_key, canonical_name, category, identifiers, source_keys, notes, registry_version) values (` +
      [
        sql(term.id),
        sql(term.canonical_name),
        sql(term.category),
        jsonSql(term.identifiers ?? {}),
        jsonSql(term.source_keys ?? []),
        sql(term.notes ?? ""),
        sql(termIndex.registry_version)
      ].join(", ") +
      `) on conflict (term_key) do update set canonical_name = excluded.canonical_name, category = excluded.category, identifiers = excluded.identifiers, source_keys = excluded.source_keys, notes = excluded.notes, registry_version = excluded.registry_version;`
  );

  for (const alias of term.aliases ?? []) {
    lines.push(
      `insert into public.term_aliases (term_key, alias_value, normalized_alias, alias_type, language, jurisdiction, confidence, source, note) values (` +
        [
          sql(term.id),
          sql(alias.value),
          sql(alias.normalized),
          sql(alias.type ?? "alias"),
          sql(alias.language ?? "und"),
          sql(alias.jurisdiction ?? "GLOBAL"),
          Number(alias.confidence ?? 0.85).toFixed(3),
          sql(alias.source ?? "curated"),
          sql(alias.note ?? null)
        ].join(", ") +
        `) on conflict (term_key, normalized_alias, language, jurisdiction, alias_type) do update set alias_value = excluded.alias_value, confidence = excluded.confidence, source = excluded.source, note = excluded.note;`
    );
  }
}

for (const link of termIndex.term_rule_links ?? []) {
  lines.push(
    `insert into public.term_rule_links (term_key, rule_code, jurisdiction, match_basis, confidence, metadata) values (` +
      [
        sql(link.term_id),
        sql(link.rule_code),
        sql(link.jurisdiction ?? "TW"),
        sql(link.match_basis),
        Number(link.confidence ?? 0.85).toFixed(3),
        jsonSql({ rules_generated_at: termIndex.rules_generated_at, source: "term-index" })
      ].join(", ") +
      `) on conflict (term_key, rule_code) do update set jurisdiction = excluded.jurisdiction, match_basis = excluded.match_basis, confidence = excluded.confidence, metadata = excluded.metadata;`
  );
}

lines.push("", "commit;", "");

await writeFile(outPath, lines.join("\n"), "utf8");
console.log(
  JSON.stringify(
    {
      output: "supabase/knowledge-seed.sql",
      sources: index.results.length,
      terms: termIndex.terms?.length ?? 0,
      aliases: (termIndex.terms ?? []).reduce((count, term) => count + (term.aliases?.length ?? 0), 0),
      ruleLinks: termIndex.term_rule_links?.length ?? 0,
      updateCandidates: updateQueue.items?.length ?? 0
    },
    null,
    2
  )
);
