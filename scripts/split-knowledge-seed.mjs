import { mkdir, readFile, rm, writeFile } from "node:fs/promises";
import path from "node:path";

const root = process.cwd();
const inputPath = path.join(root, "supabase", "knowledge-seed.sql");
const outputDir = path.join(root, "supabase", "generated", "knowledge-seed-chunks");
const maxBytes = Number(process.env.KNOWLEDGE_SQL_CHUNK_BYTES ?? 225_000);
const chunkWrapperReserveBytes = 192;
const statementBudgetBytes = Math.max(1, maxBytes - chunkWrapperReserveBytes);

const source = await readFile(inputPath, "utf8");
const statements = source
  .split(/\r?\n/)
  .map((line) => line.trim())
  .filter((line) => line && !line.startsWith("--"))
  .filter((line) => line.toLowerCase() !== "begin;" && line.toLowerCase() !== "commit;");

if (statements.length === 0) {
  throw new Error("No SQL statements found in supabase/knowledge-seed.sql");
}

const chunks = [];
let current = [];
let currentBytes = 0;

function statementBytes(statement) {
  return Buffer.byteLength(`${statement}\n`, "utf8");
}

for (const statement of statements) {
  const size = statementBytes(statement);
  const wouldExceed = current.length > 0 && currentBytes + size > statementBudgetBytes;

  if (wouldExceed) {
    chunks.push(current);
    current = [];
    currentBytes = 0;
  }

  current.push(statement);
  currentBytes += size;
}

if (current.length > 0) {
  chunks.push(current);
}

await rm(outputDir, { recursive: true, force: true });
await mkdir(outputDir, { recursive: true });

const manifest = {
  input: "supabase/knowledge-seed.sql",
  output_dir: "supabase/generated/knowledge-seed-chunks",
  max_bytes: maxBytes,
  statement_budget_bytes: statementBudgetBytes,
  chunk_count: chunks.length,
  statement_count: statements.length,
  chunks: []
};

for (const [index, chunk] of chunks.entries()) {
  const number = String(index + 1).padStart(3, "0");
  const filename = `${number}.sql`;
  const body = [
    "-- Generated by scripts/split-knowledge-seed.mjs",
    `-- Chunk ${index + 1} of ${chunks.length}; run chunks in filename order.`,
    "begin;",
    ...chunk,
    "commit;",
    ""
  ].join("\n");

  await writeFile(path.join(outputDir, filename), body, "utf8");
  manifest.chunks.push({
    file: `supabase/generated/knowledge-seed-chunks/${filename}`,
    statements: chunk.length,
    bytes: Buffer.byteLength(body, "utf8")
  });
}

await writeFile(path.join(outputDir, "manifest.json"), `${JSON.stringify(manifest, null, 2)}\n`, "utf8");

